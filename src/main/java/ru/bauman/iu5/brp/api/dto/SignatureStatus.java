package ru.bauman.iu5.brp.api.dto;

/**
 * Статус проверки цифровой подписи (ТЗ п. 5.3.2).
 */
public enum SignatureStatus {
    /**
     * Цифровая подпись подтверждена (сообщение от легитимного отправителя).
     */
    VERIFIED,

    /**
     * Цифровая подпись не подтверждена (возможна подмена).
     */
    UNVERIFIED,

    /**
     * Проверка подписи не применима (например, для исходящих сообщений).
     */
    NOT_APPLICABLE
}

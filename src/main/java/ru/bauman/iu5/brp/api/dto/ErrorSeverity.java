package ru.bauman.iu5.brp.api.dto;

/**
 * Уровень критичности ошибки.
 */
public enum ErrorSeverity {
    /**
     * Информационное сообщение (не ошибка).
     */
    INFO,

    /**
     * Предупреждение (работа продолжается).
     */
    WARNING,

    /**
     * Ошибка (частичная потеря функциональности).
     */
    ERROR,

    /**
     * Критическая ошибка (полная потеря функциональности).
     */
    CRITICAL
}

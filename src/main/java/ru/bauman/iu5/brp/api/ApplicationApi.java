package ru.bauman.iu5.brp.api;

import ru.bauman.iu5.brp.api.dto.*;
import ru.bauman.iu5.brp.api.events.ApplicationEventListener;

import java.nio.file.Path;
import java.security.KeyPair;
import java.security.PublicKey;
import java.util.List;
import java.util.Optional;

/**
 * Главный API-интерфейс прикладного уровня BRP Messenger.
 * Предоставляет функции для взаимодействия UI с сетевым стеком.
 *
 * Соответствует требованиям ТЗ п. 5.2.4 (прикладной уровень).
 */
public interface ApplicationApi {

    // ========================================================================================
    // Управление сетью и жизненным циклом
    // ========================================================================================

    /**
     * Запускает сетевой стек.
     *
     * @param tcpPort TCP-порт для входящих соединений (требование ТЗ п. 5.2.4)
     * @param useUdpDiscovery true - автообнаружение через UDP Broadcast,
     *                        false - только ручное добавление узлов (требование ТЗ п. 5.2.4)
     * @throws NetworkException если порт уже занят или ошибка инициализации
     */
    void start(int tcpPort, boolean useUdpDiscovery) throws NetworkException;

    /**
     * Останавливает сетевой стек, закрывает все соединения.
     * Вызывает Uplink-кадры для корректного разрыва логического кольца (ТЗ п. 5.2.3).
     */
    void stop();

    /**
     * Проверяет, запущен ли сетевой стек.
     *
     * @return true если запущен
     */
    boolean isRunning();

    /**
     * Подключается к удалённому узлу вручную (ручной ввод IP-адреса, ТЗ п. 5.2.4).
     *
     * @param ipAddress IP-адрес удалённого узла
     * @param port TCP-порт удалённого узла
     * @throws NetworkException если не удалось установить соединение
     */
    void connectToNode(String ipAddress, int port) throws NetworkException;

    /**
     * Отключается от узла (разрыв логического соединения через Uplink-кадр, ТЗ п. 5.2.3).
     *
     * @param nodeId идентификатор узла
     */
    void disconnectFromNode(long nodeId);

    // ========================================================================================
    // Отправка данных
    // ========================================================================================

    /**
     * Отправляет текстовое сообщение узлу (режим 1-on-1, ТЗ п. 5.1).
     * Сообщение шифруется E2E и подписывается цифровой подписью (ТЗ п. 4, 5.2.4).
     *
     * @param targetNodeId идентификатор узла-получателя
     * @param text текст сообщения
     * @return идентификатор отправленного сообщения (для отслеживания статуса доставки)
     * @throws NetworkException если узел недоступен или сеть не запущена
     */
    String sendMessage(long targetNodeId, String text) throws NetworkException;

    /**
     * Отправляет файл узлу (режим 1-on-1, ТЗ п. 5.1, 5.3.1).
     * Файл шифруется E2E и подписывается (ТЗ п. 4, 5.2.4).
     *
     * @param targetNodeId идентификатор узла-получателя
     * @param filePath путь к файлу (размер не более 100 МБ, ТЗ п. 5.3.1)
     * @return идентификатор передачи файла (для отслеживания прогресса)
     * @throws NetworkException если узел недоступен
     * @throws IllegalArgumentException если файл больше 100 МБ или не существует
     */
    String sendFile(long targetNodeId, Path filePath) throws NetworkException;

    /**
     * Отменяет передачу файла.
     *
     * @param transferId идентификатор передачи
     */
    void cancelFileTransfer(String transferId);

    // ========================================================================================
    // Получение данных о сети и узлах
    // ========================================================================================

    /**
     * Возвращает список всех обнаруженных узлов в кольце.
     *
     * @return список узлов (живые и недавно отключившиеся)
     */
    List<NodeDto> getAllNodes();

    /**
     * Возвращает список только онлайн-узлов.
     *
     * @return список активных узлов
     */
    List<NodeDto> getOnlineNodes();

    /**
     * Возвращает информацию об узле по ID.
     *
     * @param nodeId идентификатор узла
     * @return информация об узле или Optional.empty() если не найден
     */
    Optional<NodeDto> getNodeById(long nodeId);

    /**
     * Возвращает ID текущего узла.
     *
     * @return идентификатор локального узла
     */
    long getLocalNodeId();

    /**
     * Возвращает текущую топологию кольца (упорядоченный список узлов).
     *
     * @return список ID узлов в порядке следования в кольце
     */
    List<Long> getRingTopology();

    // ========================================================================================
    // История сообщений (персистентное хранилище, ТЗ п. 5.2.4)
    // ========================================================================================

    /**
     * Возвращает историю сообщений с конкретным узлом.
     * Загружается из персистентного хранилища (ТЗ п. 5.2.4).
     *
     * @param nodeId идентификатор собеседника
     * @param limit максимальное количество сообщений (0 = все)
     * @param offset смещение для пагинации
     * @return список сообщений (от старых к новым)
     */
    List<ChatMessageDto> getMessageHistory(long nodeId, int limit, int offset);

    /**
     * Возвращает последнее сообщение с каждым узлом (для превью в списке чатов).
     *
     * @return мапа nodeId -> последнее сообщение
     */
    List<ChatMessageDto> getLastMessages();

    /**
     * Возвращает количество непрочитанных сообщений от узла.
     *
     * @param nodeId идентификатор узла
     * @return количество непрочитанных
     */
    int getUnreadCount(long nodeId);

    /**
     * Отмечает все сообщения от узла как прочитанные.
     *
     * @param nodeId идентификатор узла
     */
    void markAsRead(long nodeId);

    /**
     * Удаляет историю сообщений с узлом.
     *
     * @param nodeId идентификатор узла
     */
    void deleteHistory(long nodeId);

    // ========================================================================================
    // Статусы доставки (ТЗ п. 5.2.4, 5.3.2)
    // ========================================================================================

    /**
     * Возвращает статус доставки сообщения.
     *
     * @param messageId идентификатор сообщения
     * @return статус (SENT/DELIVERED/ERROR)
     */
    Optional<DeliveryStatus> getMessageDeliveryStatus(String messageId);

    /**
     * Возвращает прогресс передачи файла.
     *
     * @param transferId идентификатор передачи
     * @return информация о передаче или Optional.empty()
     */
    Optional<FileTransferDto> getFileTransferProgress(String transferId);

    /**
     * Возвращает список активных передач файлов.
     *
     * @return список передач
     */
    List<FileTransferDto> getActiveFileTransfers();

    // ========================================================================================
    // Настройки (ТЗ п. 5.2.4)
    // ========================================================================================

    /**
     * Устанавливает каталог для сохранения принятых файлов (ТЗ п. 5.2.4).
     *
     * @param directory путь к каталогу
     * @throws IllegalArgumentException если каталог не существует или нет прав записи
     */
    void setDownloadDirectory(Path directory);

    /**
     * Возвращает текущий каталог для загрузок.
     *
     * @return путь к каталогу
     */
    Path getDownloadDirectory();

    /**
     * Устанавливает отображаемое имя локального узла.
     *
     * @param name имя пользователя
     */
    void setLocalNodeName(String name);

    /**
     * Возвращает имя локального узла.
     *
     * @return имя
     */
    String getLocalNodeName();

    // ========================================================================================
    // Криптография (ТЗ п. 5.2.4)
    // ========================================================================================

    /**
     * Возвращает пару ключей локального узла.
     * Генерируется при первом запуске и хранится безопасно (ТЗ п. 5.2.4).
     *
     * @return пара ключей (публичный/приватный)
     */
    KeyPair getLocalKeyPair();

    /**
     * Возвращает публичный ключ удалённого узла.
     * Получается при обмене при установлении соединения (ТЗ п. 5.2.4).
     *
     * @param nodeId идентификатор узла
     * @return публичный ключ или Optional.empty() если узел не обменялся ключами
     */
    Optional<PublicKey> getNodePublicKey(long nodeId);

    /**
     * Проверяет, обменялись ли узлы публичными ключами.
     *
     * @param nodeId идентификатор узла
     * @return true если ключ получен
     */
    boolean hasPublicKey(long nodeId);

    // ========================================================================================
    // События (ТЗ п. 5.3.2 - системные уведомления)
    // ========================================================================================

    /**
     * Подписывается на события сети и доставки сообщений.
     * События включают (ТЗ п. 5.3.2):
     * - подключение/отключение узлов
     * - реконфигурация кольца
     * - разрыв целостности кольца
     * - доставка/недоставка сообщений
     * - прием сообщений и файлов
     * - прогресс передачи файлов
     *
     * @param listener слушатель событий
     */
    void addEventListener(ApplicationEventListener listener);

    /**
     * Отписывается от событий.
     *
     * @param listener слушатель для удаления
     */
    void removeEventListener(ApplicationEventListener listener);

    // ========================================================================================
    // Диагностика и статистика
    // ========================================================================================

    /**
     * Возвращает статистику сети.
     *
     * @return статистика (отправлено/получено байт, кадров, ошибок)
     */
    NetworkStatistics getNetworkStatistics();

    /**
     * Возвращает последние ошибки сети для отображения пользователю.
     *
     * @param limit максимальное количество ошибок
     * @return список ошибок
     */
    List<NetworkError> getRecentErrors(int limit);
}
